            <h4>Focus sulle imposte nella bolletta gas</h4>
            <p>Per <strong>leggere le bollette del gas</strong> bisogna sapere che la bolletta deve indicare
                l’aliquota
                delle imposte, applicata secondo la normativa. &nbsp;Nel caso della bolletta gas vengono applicate
                l’<strong>accisa</strong>, l’<strong>addizionale regionale</strong> e l’<strong>IVA</strong>:<br>
            </p>
            <ul class="shortcode shortcode--lugia list list--custom-cross ">
                <li><strong>Accisa</strong>: (o chiamata anche imposta diretta sul consumo) è un’imposta che si paga
                    sul
                    consumo di gas, indipendentemente dal fornitore scelto. Per quanto riguarda gli “usi civili” si
                    differenzia in due macro zone del paese (Centro-Nord e Sud). L’ammontare dell’Accisa è minore al
                    sud
                    per
                    agevolare la creazione della rete di distribuzione. L’Accisa è articolata su <strong>diverse
                        aliquote</strong> e si divide in <strong>4 scaglioni annuali di consumo e aree
                        geografiche</strong>
                    per quanto riguarda le utenze domestiche. L’importo dell’accisa sarà quindi calcolato in
                    funzione al
                    consumo di gas fatturato in Smc.</li>
                <li><strong>Addizionale Regionale</strong>: è un’imposta determinata da ciascuna regione nei limiti
                    stabiliti da Arera, per tutte le regioni tranne per quelle a statuto speciale.&nbsp;Esattamente
                    come
                    l’accisa, anche l’addizionale regionale si calcola per fasce annuali di consumo e per categorie
                    d’utilizzo.</li>
                <li><strong>IVA</strong>: l’Imposta sul Valore Aggiunto si calcola sul totale di tutte le altre voci
                    in
                    bolletta, Accisa compresa (sì, si paga un imposta su un’imposta!). L’aliquota applicata all’uso
                    domestico è del 10% (fino al raggiungimento di 480 Smc all’anno consumati). E del 22% su tutti
                    gli
                    altri
                    consumi e per usi diversi da quello domestico.</li>
            </ul>
