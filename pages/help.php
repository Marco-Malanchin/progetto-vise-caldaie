            <p>Andiamo a vedere nel dettaglio quali sono le informazioni della sezione “dati fornitura” della
                bolletta:<br>
            </p>
            <ol class="shortcode shortcode--lugia list list--steps ">
                <li><strong>Indirizzo della fornitura di gas</strong>: talvolta denominata “forniamo gas in:”
                    contiene
                    l’indirizzo dell’abitazione o dell’immobile dove è attiva l’utenza gas.</li>
                <li><strong>Tipologia di cliente</strong>: può per esempio essere un cliente domestico o un
                    condominio
                    con
                    uso domestico, o altre tipologie per le categorie professionali. <em>(*Un cliente domestico si
                        contraddistingue per essere un contratto intestato a persona fisica, che usa il gas da un
                        unico
                        punto di riconsegna, per alimentare un’abitazione.)</em></li>
                <li><strong>Tipologia di uso</strong>: dipende dall’uso del gas dichiarato nel contratto, e possono
                    essere
                    una o più delle seguenti voci: acqua calda sanitaria, cottura fornelli a gas, riscaldamento.
                </li>
                <li><strong>Denominazione del contratto</strong>: contiene il nome del contratto attivo per al tua
                    fornitura
                    e la tua bolletta gas</li>
                <li><a href="https://www.prontobolletta.it/lettura-contatore-gas/matricola/"><strong>Matricola del
                            contatore</strong></a>: è il codice che identifica il contatore, viene aggiunto alla
                    fabbricazione.</li>
                <li><strong>Classe del misuratore</strong>: il contatore può appartenere ad una diversa classe in
                    base
                    alla
                    potenza misurabile. Per uso domestico le classi più diffuse sono G4 e G6. Per uso condominiale
                    possiamo
                    trovare spesso la classe G10, mentre per uso industriale sono utilizzate classi da G16 fino a
                    G6500.
                </li>
                <li><strong>Codice REMI</strong>: è un codice che identifica il punto di consegna dell’impianto di
                    distribuzione che identifica il punto di riconsegna del contatore. La cosiddetta cabina o
                    centralina
                    del
                    gas.</li>
                <li><strong>Tipologia di pagamento</strong>: specifica come il cliente paga le sue bollette gas. le
                    opzioni
                    possono essere per addebito automatico su conto corrente presso l’istituto bancario del cliente,
                    o
                    pagamento postale, o pagamento in contanti. E ancora, il metodo di pagamento può essere: al
                    giorno
                    esatto o rateizzato.</li>
                <li><strong>Parametro P</strong>: che rappresenta il potere energetico del gas, più il parametro P è
                    alto e
                    più il prezzo del gas sarà maggiore. Il Parametro P è l’ammontare di energia contenuto in un
                    metro
                    cubo
                    di gas, e viene <strong>comunicato mensilmente da Snam</strong> per ogni cabina REMI.</li>
                <li><strong>Coefficiente C</strong>: è il coefficiente che serve a convertire il consumo di gas del
                    contatore in metri cubi, in Smc, ovvero Standard Metri Cubi.</li>
                <li><strong>Data di attivazione della fornitura</strong>: è la data del giorno in cui è stata
                    attivata
                    l’utenza.</li>
                <li><strong>Componente sostitutiva materia prima gas</strong>: (puoi trovarlo scritto anche come:
                    <em>comp.sost.mat.prima contratto</em>) è il prezzo del gas concordato tra il cliente ed il
                    fornitore al
                    momento della stipula del contratto.
                </li>
                <li><strong>Distributore di riferimento</strong>: indica il nome della società che si occupa della
                    <a href="https://www.prontobolletta.it/differenza-fornitori-distributori-gestori/">distribuzione
                        del
                        gas</a> nel tuo territorio.
                </li>
                <li><a href="https://www.prontobolletta.it/lettura-contatore-luce/numero-cliente/"><strong>Codice
                            Cliente</strong></a>: identifica il cliente e intestatario dell’utenza, è un numero
                    generalmente
                    a 9 cifre.</li>
                <li><strong>Codice PDR</strong>: (<a
                        href="https://www.prontobolletta.it/lettura-contatore-gas/pdr/">Punto
                        di Riconsegna</a>), ovvero il codice che designa la posizione fisica del contatore del
                    cliente,
                    ovvero il punto in cui il gas viene consegnato dal fornitore e prelevato dal cliente finale</li>
                <li><strong>Punto di Consegna (PDC)</strong>: Punto di connessione tra rete di trasporto gas e rete
                    di
                    distribuzione</li>
                <li><strong>Codice Fiscale</strong> del cliente</li>
                <li><strong>Dati bolletta</strong>: con le informazioni riguardanti la singola fattura, come data di
                    fatturazione e numero della fattura.</li>
                <li><strong>Periodo</strong>: indica le date di inizio e fine del periodo di tempo a cui si
                    riferisce
                    questa
                    bolletta.</li>
                <li><strong>Totale da pagare</strong>: con la spesa totale del tempo incluso nel periodo soggetto al
                    pagamento della bolletta. Ed entro quando pagarli, con indicata la scadenza della fattura.</li>
            </ol>
            <p><br>
                Nella parte dei dati della bolletta gas i clienti trovano quindi un riepilogo di tutti i dati del
                cliente e
                della fornitura.</p>
